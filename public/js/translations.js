// Apply translations to all elements with data-i18n attribute
function applyLanguage(lang) {
  if (!lang) lang = localStorage.getItem('ub_language') || 'en';
  const dict = TRANSLATIONS[lang] || TRANSLATIONS.en;

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (dict[key]) {
      // For inputs, set placeholder; for others, set textContent
      if (el.tagName === 'INPUT' && el.hasAttribute('placeholder')) {
        el.placeholder = dict[key];
      } else {
        // Preserve icons — only replace text nodes
        const icon = el.querySelector('i, svg');
        if (icon) {
          // Clear text nodes, keep icon
          const nodes = Array.from(el.childNodes);
          nodes.forEach(n => { if (n.nodeType === 3) n.remove(); });
          el.appendChild(document.createTextNode(' ' + dict[key]));
        } else {
          el.textContent = dict[key];
        }
      }
    }
  });

  localStorage.setItem('ub_language', lang);

  // Update language selectors if present
  document.querySelectorAll('#langSelect, #settingsLang').forEach(sel => {
    if (sel) sel.value = lang;
  });
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', () => {
  const savedLang = localStorage.getItem('ub_language') || 'en';
  if (savedLang !== 'en') {
    applyLanguage(savedLang);
  }
});
